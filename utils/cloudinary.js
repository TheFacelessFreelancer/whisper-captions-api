export async function uploadToCloudinary(filePath, publicId) {
  logProgress('ğŸ“¤ Uploading video to Cloudinary...', { filePath, publicId });

  return new Promise((resolve, reject) => {
    cloudinary.v2.uploader.upload(
      filePath,
      {
        resource_type: 'video',
        public_id: publicId,
        overwrite: true,
        folder: 'Caption-Factory-App-Videos',
      },
      (error, result) => {
        if (error) {
          logError('âŒ Cloudinary upload failed', error);
          reject(error);
        } else {
          logInfo('âœ… Uploaded to Cloudinary:', result.secure_url);
          resolve(result.secure_url);
        }
      }
    );
  });
}
